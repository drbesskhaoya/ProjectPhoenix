# Store text values in variables
name = "Bess"
profession = "Doctor"
goal = "Healthcare AI Specialist"

# Display the values stored in the variables
print("Name:", name)
print("Profession:", profession)
print("Career Goal:", goal)

# ------------------------------------------------------------
# Key Takeaways
# ------------------------------------------------------------
# 1. A variable stores information.
# 2. Strings are pieces of text enclosed in quotation marks.
# 3. Variable names should be meaningful and descriptive.
# 4. print() displays information on the screen.
# 5. Writing print(name) displays the value of the variable.
# 6. Writing print("name") displays the word "name" literally.
