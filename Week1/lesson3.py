# Store personal information in variables
age = 30
years_as_doctor = 6

# Display the values
print("Age:", age)
print("Years as a doctor:", years_as_doctor)

# Perform a calculation
average = age / years_as_doctor

# Display the result
print("Age ÷ Years as a doctor =", average)

# ------------------------------------------------------------
# Key Takeaways
# ------------------------------------------------------------
# 1. Variables store information.
# 2. Numbers can be used in mathematical calculations.
# 3. The '/' operator performs division and returns a float.
# 4. Results of calculations can be stored in new variables.
